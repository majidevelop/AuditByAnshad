                    document.getElementById("save_header").addEventListener("click", function (e) {
    e.preventDefault();
    const editorElement = document.querySelector('.ck.ck-content.ck-editor__editable.ck-rounded-corners.ck-editor__editable_inline.ck-blurred');

if (editorElement) {
    const content = editorElement.innerHTML;
    console.log(content); // Do what you want with the content
} else {
    console.log("Editor element not found.");
}

    const editorData = editorElement.innerHTML;

    if (!editorData.trim()) {
        alert("Please enter header content.");
        return;

    }
    let header_name = document.getElementById('header_name').value;
    let header_desc = document.getElementById('header_desc').value;

    const payload = {
        header_text:editorData,
        header_name:header_name,
        header_desc:header_desc
    };
    console.log(payload);

    fetch("ajax/save_header.php", {
        method: "POST",
        headers: {
            "Content-Type": "application/x-www-form-urlencoded",
        },
        body: JSON.stringify(payload)
     
    })
    .then(res => res.json())

    .then(data => {
        console.log(data);
    })
    .catch(err => {
        console.error("AJAX error:", err);
        alert("Something went wrong.");
    });
});

